    /**
     * تطبيق دالة الترجمة على نص
     */
    public function applyTranslation($text) {
        // البحث عن النص في قائمة الترجمات الشائعة
        if (isset($this->commonTranslations[$text])) {
            return '__("' . $this->commonTranslations[$text] . '")';
        }
        
        // إذا لم يوجد، إنشاء مفتاح ترجمة
        $key = $this->generateTranslationKey($text);
        return '__("' . $key . '")';
    }
    
    /**
     * إنشاء مفتاح ترجمة من النص
     */
    private function generateTranslationKey($text) {
        $key = strtolower($text);
        $key = preg_replace('/[^a-z0-9\s]/', '', $key);
        $key = preg_replace('/\s+/', '_', $key);
        $key = trim($key, '_');
        return $key;
    }
    
    /**
     * عرض قائمة بالترجمات الشائعة
     */
    public function showCommonTranslations() {
        echo "Common translations available:\n";
        echo "============================\n";
        foreach ($this->commonTranslations as $english => $key) {
            echo sprintf("%-30s => %s\n", $english, $key);
        }
    }
